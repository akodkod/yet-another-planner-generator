export function consoleLog(...args: unknown[]) {
  // oxlint-disable-next-line no-console
  console.log(...args)
}

export function consoleWarn(...args: unknown[]) {
  // oxlint-disable-next-line no-console
  console.warn(...args)
}

export function consoleError(...args: unknown[]) {
  // oxlint-disable-next-line no-console
  console.error(...args)
}
